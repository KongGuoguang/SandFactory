public class StatisticQueryDetailItem {
    private String hm;

    private float weight;

    private float car;

    private float money;


    public String getHm() {
        return hm;
    }

    public void setHm(String hm) {
        this.hm = hm;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public float getCar() {
        return car;
    }

    public void setCar(float car) {
        this.car = car;
    }

    public float getMoney() {
        return money;
    }

    public void setMoney(float money) {
        this.money = money;
    }
}
